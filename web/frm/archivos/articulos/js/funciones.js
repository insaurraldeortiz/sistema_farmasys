function buscarIdArticulo() {
    var datosFormulario = $("#formPrograma").serialize();
    $.ajax({
        type: 'POST',
        url: 'jsp/buscarId.jsp',
        data: datosFormulario,
        dataType: 'json',
        beforeSend: function (objeto) {
            $("#mensajes").html("Enviando datos al Servidor ...");
        },
        success: function (json) {
            $("#mensajes").html(json.mensaje);
            $("#id_articulo").val(json.id_articulo);
            $("#codigo_articulo").val(json.codigo_articulo);
            $("#nombre_articulo").val(json.nombre_articulo);
            $("#preciocompra_articulo").val(json.preciocompra_articulo);
            $("#precioventa_articulo").val(json.precioventa_articulo);
            $("#iva_articulo").val(json.iva_articulo);

            $("#id_marca").val(json.id_marca);
            $("#nombre_marca").val(json.nombre_marca);

            $("#id_rubro").val(json.id_rubro);
            $("#nombre_rubro").val(json.nombre_rubro);

            $("#id_categoria").val(json.id_categoria);
            $("#nombre_categoria").val(json.nombre_categoria);

            $("#id_presentacion").val(json.id_presentacion);
            $("#nombre_presentacion").val(json.nombre_presentacion);

            $("#id_proveedor").val(json.id_proveedor);
            $("#nombre_proveedor").val(json.nombre_proveedor);

            if (json.nuevo === "true") {
                $("#botonAgregar").prop('disabled', false);
                $("#botonModificar").prop('disabled', true);
                $("#botonEliminar").prop('disabled', true);
                siguienteCampo("#nombre_articulo", "#botonAgregar", true);
            } else {
                $("#botonAgregar").prop('disabled', true);
                $("#botonModificar").prop('disabled', false);
                $("#botonEliminar").prop('disabled', true);
                siguienteCampo("#nombre_articulo", "#botonModificar", true);
            }
        },
        error: function (e) {
            $("#mensajes").html("No se pudo modificar los datos.");
        },
        complete: function (objeto, exito, error) {
            if (exito === "success") {
            }
        }
    });
}

